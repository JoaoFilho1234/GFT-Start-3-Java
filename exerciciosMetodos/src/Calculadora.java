public class Calculadora {

    public static void soma(double a, double b) {
        double resultado = a + b;
        System.out.println("A soma de "+ a + " mais " + b + " é igual "+resultado );
    }

    public static void subtracao(double a, double b) {
        double resultado = a - b;
        System.out.println("O resultado da subtração de "+a+ " mais "+b+ " é igual "+resultado);
    }

    public static void multiplicação(double a, double b) {
        double resultado = a * b;
        System.out.println("O resultado da multiplicação de "+a+ " por "+b+" é "+resultado );
    }

    public static void divisao(double a, double b) {
        double resultado = a / b;
        System.out.println("O resultado da divisão de "+a+" por "+b+" é "+resultado);
    }





}
